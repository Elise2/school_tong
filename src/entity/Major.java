package entity;

public class Major {
	private int id;
	private String idEncode;
	private String name;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getIdEncode() {
		return idEncode;
	}

	public void setIdEncode(String idEncode) {
		this.idEncode = idEncode;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
